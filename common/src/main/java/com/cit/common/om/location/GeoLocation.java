package com.cit.common.om.location;

/**
 * Class to represent a geographical location
 */
public class GeoLocation {
    private float x;
    private float y;
    private float z;

    public GeoLocation() {
    }

    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }

    public float getZ() {
        return z;
    }

    public void setZ(float z) {
        this.z = z;
    }
}
